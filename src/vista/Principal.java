    private void actualizarBotonesPorSeleccion()
    {
        boolean seleccionado = checkAlum.isSelected() || checkPer.isSelected();

        // En lugar de mandar todos a la vez a CtrlInterfaz.habilita,
        // evaluamos cada uno contra su permiso de rol usando (seleccionado && estadoDelRol)
        // Si tu CtrlInterfaz.habilita acepta un solo componente:
        CtrlInterfaz.habilita(seleccionado && estadoRegistro, btnRegistro);
        CtrlInterfaz.habilita(seleccionado && estadoConsulta, btnConsulta);
        CtrlInterfaz.habilita(seleccionado && estadoModif, btnModif);
        CtrlInterfaz.habilita(estadoBackup, btnCopiaS);

        // Habilitar botón de Cita Médica
        btnNc.setEnabled(seleccionado && estadoNc);
    }

    private void configurarAccesosSegunRol()
    {
        switch (SesionActual.getInstance().getRol())
        {
            case "Administrador":
                // Nivel 3: Todos los permisos (rwx)
                estadoRegistro = true;
                estadoConsulta = true;
                estadoModif = true;
                estadoNc = true;
                estadoLogs = true;
                estadoBackup = true;
                break;

            case "Medico":
                // Nivel 2: Operativo Clínico (rw-)
                estadoRegistro = true;
                estadoConsulta = true;
                estadoModif = true;
                estadoNc = true;
                estadoLogs = false;   // Sin acceso al sistema
                estadoBackup = false; // Sin acceso al sistema
                break;

            case "Asistente":
                // Nivel 1: Solo captura y lectura (r--)
                estadoRegistro = true;
                estadoConsulta = true;
                estadoModif = false; // No puede alterar expedientes
                estadoNc = false;    // No da consultas médicas
                estadoLogs = false;
                estadoBackup = false;
                break;
        }

        // Los botones de sistema (Logs/Backup) no dependen de los CheckBox, 
        // así que los podemos desaparecer de la interfaz desde aquí:
        // btnLogs.setVisible(estadoLogs);
        // btnBackup.setVisible(estadoBackup);
    }

