    private void btnNcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNcActionPerformed

        // 1. BARRERA DE SEGURIDAD (Control de Roles mediante el Singleton)
        String rolActual = SesionActual.getInstance().getRol();
        if (!rolActual.equals("Administrador") && !rolActual.equals("Medico"))
        {
            JOptionPane.showMessageDialog(this,
                    "Acceso Denegado: Su rol actual ('" + rolActual + "') no tiene permisos para modificar expedientes.",
                    "Bloqueo de Seguridad", JOptionPane.WARNING_MESSAGE);
            return; // Cortamos la ejecución aquí, no se le pide ninguna clave
        }

        boolean seguirBuscando = true;

        // 2. CICLO DE BÚSQUEDA
        while (seguirBuscando)
        {

            String mensaje = (Principal.tipo == 1) ? "Clave del alumno:" : "Clave del personal:";
            String bCve = JOptionPane.showInputDialog(this, mensaje, "Buscar para Modificar", JOptionPane.QUESTION_MESSAGE);

            // Si el usuario presiona "Cancelar" o la 'X' de la ventana
            if (bCve == null)
            {
                break;
            }

            // Si el usuario le da "Aceptar" pero dejó el cuadro en blanco
            if (bCve.trim().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "No se ingresó ninguna clave.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                continue; // Regresa al inicio del while y vuelve a preguntar
            }

            // Validación preventiva: ¿Lo que escribió son puros números?
            if (!bCve.matches("\\d+"))
            {
                JOptionPane.showMessageDialog(this, "Entrada no válida. Ingrese únicamente números.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
                continue; // Regresa al inicio del while
            }

            // 3. COMUNICACIÓN CON EL CONTROLADOR
            // Pasamos el string limpio y validado a nuestro objeto Controlador
            Object[] datosPersona = miControlador.obtenerTodosLosDatos(bCve, this);

            // 4. APERTURA DE INTERFAZ
            if (datosPersona != null)
            {
                // Se encontró la persona, abrimos la ventana inyectando los datos
                if (Principal.tipo == 1)
                {
                    HistClinico m = new HistClinico(datosPersona);
                    m.setVisible(true);
                } else
                {
                    HistClinico m = new HistClinico(datosPersona);
                    m.setVisible(true);
                }

                // Rompemos el ciclo porque ya cumplimos el objetivo
                seguirBuscando = false;
            }
            // Si el Controlador devuelve null (porque no encontró la clave en la BD),
            // el Controlador ya mostró su propio mensaje de "Clave no encontrada",
            // y el ciclo 'while' simplemente volverá a pedir la clave.
        }
    }//GEN-LAST:event_btnNcActionPerformed

