    private void siguineteRAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siguineteRAActionPerformed

        if (checkOtras.isSelected() == true && jxtaOtras.getText().length() < 2)
        {
            Mensajes.error(this, "Debe especificar <otras>");

        } else
        {
            if (jPanel3.isVisible())
            {
                obj[1] = (jTNombre.getText());
                obj[2] = (jTPA.getText());
                obj[3] = (jTSA.getText());
            }
            Object[] persona =
            {
                obj[0], obj[1], obj[2], obj[3], des, sob, aler, obec,
                diab, jxtaOtras.getText(), cbxEstatus.getSelectedItem()
            };
            try
            {
                AccessDBConnection.modificarD(obj[0].toString(), this, "P", persona);

            } catch (SQLException e)
            {
                System.out.println("A");
            }
            this.dispose();

        }

    }//GEN-LAST:event_siguineteRAActionPerformed

