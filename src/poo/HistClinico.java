    private void GuardarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_GuardarActionPerformed
    {//GEN-HEADEREND:event_GuardarActionPerformed
        // TODO add your handling code here:

        if (Validaciones.validaLongitud(2, this, "Debe llenar todos los campos",
                jtxtPlan, jtxtMedic, jtxAntece, jtxtPadecim) == false)
        {
            Date hoy = new Date();
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy HH:mm", new Locale("es", "ES"));
            // Formatea la fecha en espaÃ±ol
            String fechaFormateada = formatoFecha.format(hoy);
            String url = "jdbc:ucanaccess://BD\\Proyecto2P.accdb";

            try (Connection conn = DriverManager.getConnection(url))
            {
                Object[] persona = {
                  obj[0], jtxtPadecim.getText(), jtxAntece.getText(),
                    jtxtMedic.getText(), jtxtPlan.getText()
                };
                System.out.println("Insercion de cita");
                AccessDBConnection.insertarDatosHistorial(conn, persona);
                dispose();

            } catch (SQLException e)
            {
                e.printStackTrace();
            }
            
            
        }

    }//GEN-LAST:event_GuardarActionPerformed

