    public static int[] ordenarM(HistorialClinico[][] matriz)
    {
        int filas = matriz.length;
        int[] guardaPos = new int[filas];
        for (int i = 0; i < filas; i++)
        {
            guardaPos[i] = i;
        }
        guardaPos = burbuja(guardaPos, matriz);
        return guardaPos;
    }

    public static int[] burbuja(int[] arr, HistorialClinico[][] hc)
    {

        int n = arr.length;
        int k, l;
        for (int i = 0; i < n - 1; i++)
        {
            for (int j = 0; j < n - i - 1; j++)
            {
                if (hc[arr[j]] != null)
                {
                    k = hc[arr[j]].length;
                } else
                {
                    k = 0;
                }
                if (hc[arr[j + 1]] != null)
                {
                    l = hc[arr[j + 1]].length;
                } else
                {
                    l = 0;
                }
                if (k < l)
                {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
        return arr;
    }

    public static int[] burbuja(int[] arr)
    {
        int n = arr.length;
        int k, l;
        for (int i = 0; i < n - 1; i++)
        {
            for (int j = 0; j < n - i - 1; j++)
            {
                if (arr[j] < arr[j + 1])
                {
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
        return arr;
    }

    public static int[] invertirArray(int[] array)
    {
        int longitud = array.length;
        for (int i = 0; i < longitud / 2; i++)
        {
            // Intercambiar elementos desde los extremos hacia el centro
            int temp = array[i];
            array[i] = array[longitud - 1 - i];
            array[longitud - 1 - i] = temp;
        }
        return array;
    }

