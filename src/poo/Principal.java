    private void btnNcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNcActionPerformed
        try
        {
            do
            {
                if (tipo == 1)
                {
                    bCve = JOptionPane.showInputDialog("Clave del alumno: ");

                } else
                {
                    bCve = JOptionPane.showInputDialog("Clave del personal: ");
                }
                if (bCve == null)
                {
                    break;
                } else
                {
                    if (bCve.trim().isEmpty())
                    {
                        JOptionPane.showMessageDialog(null, "No se ingreso ninguna clave", "Advertencia", JOptionPane.WARNING_MESSAGE);
                        continue;
                    }
                }
                Object[] np = Controlador.ObtenerNC(bCve, this);
                if (np != null)
                {
                    HistClinico m = new HistClinico(np);
                    m.setVisible(true);
                    break;
                }
            } while (true);
        } catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Entrada no valida, Ingrese una matricula", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnNcActionPerformed

